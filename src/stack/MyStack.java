package stack;  // папка, где лежит файл

public class MyStack {

    // ЭТО ВНУТРЕННИЙ МАССИВ, ГДЕ МЫ БУДЕМ ХРАНИТЬ ЧИСЛА
    // private — чтобы никто снаружи не лазил
    // int[] — массив целых чисел
    // data — имя переменной
    // new int[2] — создаём массив на 2 ячейки (потом расширим)
    private int[] data = new int[2];

    // ЭТО СЧЁТЧИК — СКОЛЬКО ЭЛЕМЕНТОВ РЕАЛЬНО ЛЕЖИТ В СТЕКЕ
    // size = 0 — изначально стек пуст (ни одной тарелки нет)
    private int size = 0;

    // ==========================================================
    // МЕТОД push() — ПОЛОЖИТЬ ЭЛЕМЕНТ НА ВЕРХ
    // ==========================================================
    public void push(int value) {
        // СНАЧАЛА ПРОВЕРЯЕМ: А МЕСТО ЕСТЬ?
        // size — сколько элементов лежит
        // data.length — сколько ячеек ВНУТРИ массива (сейчас 2)
        // Если количество элементов ДОСТИГЛО длины массива — значит, массив заполнен
        if (size == data.length) {

            // СОЗДАЁМ НОВЫЙ МАССИВ БОЛЬШЕГО РАЗМЕРА (в 2 раза)
            // Например: был на 2, стал на 4
            int[] newData = new int[data.length * 2];

            // КОПИРУЕМ СТАРЫЕ ЭЛЕМЕНТЫ В НОВЫЙ МАССИВ
            // i идёт от 0 до size-1 (по всем реальным элементам)
            for (int i = 0; i < size; i++) {
                newData[i] = data[i];  // перекладываем из старого в новый
            }

            // ЗАМЕНЯЕМ СТАРЫЙ МАССИВ НОВЫМ
            data = newData;
        }

        // ТЕПЕРЬ МЕСТО ТОЧНО ЕСТЬ
        // Кладём новый элемент в первую свободную ячейку
        // data[size] — берём ячейку по индексу size (первый свободный)
        data[size] = value;

        // УВЕЛИЧИВАЕМ СЧЁТЧИК (теперь элементов стало на 1 больше)
        size++;
    }

    // ==========================================================
    // МЕТОД pop() — СНЯТЬ ЭЛЕМЕНТ С ВЕРХУШКИ И ВЕРНУТЬ ЕГО
    // ==========================================================
    public int pop() {
        // ПРОВЕРЯЕМ: А ЕСТЬ ЛИ ВООБЩЕ ЭЛЕМЕНТЫ?
        // size == 0 — значит, стек пуст
        if (size == 0) {
            // ОШИБКА: стек пуст, снимать нечего
            // System.out.println — просто печатаем сообщение
            System.out.println("Ошибка: стек пуст");

            // return -1 — возвращаем -1 как "значение ошибки"
            // ПОЧЕМУ -1? Потому что это число, которого не может быть в нормальном стеке
            // (если в стеке могут быть отрицательные числа, то -1 — плохая заглушка)
            // Это ВРЕМЕННОЕ РЕШЕНИЕ для простоты
            return -1;  // "заглушка" — просто чтобы программа не упала
        }

        // УМЕНЬШАЕМ СЧЁТЧИК НА 1
        // size-- означает: size = size - 1
        // Почему УМЕНЬШАЕМ? Потому что мы снимаем один элемент со стека
        // Теперь реальных элементов стало на 1 меньше
        size--;

        // ВОЗВРАЩАЕМ ТО, ЧТО БЫЛО НА ВЕРХУ
        // После уменьшения size указывает на последний элемент
        // Например: было size=3, элементы по индексам 0,1,2
        // После size-- стало size=2, и data[2] — это последний элемент
        // Его и возвращаем
        return data[size];
    }

    // ==========================================================
    // МЕТОД peek() — ПОСМОТРЕТЬ ВЕРХНИЙ ЭЛЕМЕНТ (НЕ СНИМАЯ)
    // ==========================================================
    public int peek() {
        // Та же проверка на пустоту
        if (size == 0) {
            System.out.println("Ошибка: стек пуст");
            return -1;
        }

        // ВОЗВРАЩАЕМ ПОСЛЕДНИЙ ЭЛЕМЕНТ (но НЕ УМЕНЬШАЕМ size)
        // Почему size - 1?
        // Потому что индексы начинаются с 0
        // Если size=3, то элементы по индексам: 0, 1, 2
        // Последний индекс = size - 1 = 2
        return data[size - 1];
    }

    // ==========================================================
    // МЕТОД isEmpty() — ПРОВЕРЯЕТ, ПУСТ ЛИ СТЕК
    // ==========================================================
    public boolean isEmpty() {
        // Возвращает true, если size == 0 (стек пуст)
        // Возвращает false, если size > 0 (есть элементы)
        return size == 0;
    }

    // ==========================================================
    // МЕТОД size() — ВОЗВРАЩАЕТ КОЛИЧЕСТВО ЭЛЕМЕНТОВ
    // ==========================================================
    public int size() {
        return size;
    }

    // ==========================================================
    // МЕТОД print() — ПЕЧАТАЕТ ВЕСЬ СТЕК (для отладки)
    // ==========================================================
    public void print() {
        System.out.print("[");
        for (int i = 0; i < size; i++) {
            System.out.print(data[i]);
            if (i < size - 1) {
                System.out.print(", ");
            }
        }
        System.out.println("]");
    }
}
