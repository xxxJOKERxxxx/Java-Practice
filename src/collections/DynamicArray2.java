package collections;

import java.util.Arrays;

public class DynamicArray2 {
    // Внутренний массив для хранения данных (пока пустой)
    private int[] data = new int[2]; // new int[2] — создаём массив на 2 ячейки (начальный размер)
    private int size = 0;//  Счётчик реального количества элементов

    //— узнать количество элементов//Просто показывает, сколько элементов сейчас в массиве.
    public int size() {
        return size;// return — вернуть значение// size — переменная, в которой хранится количество элементов
    }

    //Метод get(int index) — получить элемент по индексу
    public int get(int index) {
        // Проверяем: если index меньше 0 ИЛИ index больше или равен size
        // (потому что последний допустимый индекс = size - 1)
        if (index < 0 || index >= size) {
            // выбрасываем ошибку с понятным текстом
            throw new IndexOutOfBoundsException("Индекс " + index + " вне границ. Размер: " + size);
        }
        // если проверку прошли — возвращаем элемент из внутреннего массива по индексу
        return data[index];
    }

    //Метод add(int value) — добавить элемент в конец
    public void add(int value) {
        // void — ничего не возвращает, просто добавляет

        // ПРОВЕРКА: нужно ли расширять массив?
        // Если количество элементов (size) равно длине внутреннего массива (data.length)
        // значит, места больше нет — нужно расширяться
        if (size == data.length) {

            // ШАГ 1: Запоминаем старые данные во временный массив
            // Arrays.copyOf — копирует массив
            // data — откуда копируем
            // size — сколько элементов копируем (только реальные данные, без мусора)
            int[] temp = Arrays.copyOf(data, size);

            // ШАГ 2: Увеличиваем длину массива в 2 раза
            // data.length — текущая длина (например, 2)
            // * 2 — умножаем на 2 (получаем 4)
            data = new int[data.length * 2];

            // ШАГ 3: Копируем старые данные обратно в новый массив
            // проходим по всем элементам temp (старый массив)
            for (int i = 0; i < temp.length; i++) {
                // кладём каждый элемент на ту же позицию в новый массив
                data[i] = temp[i];
            }
        }

        // теперь место точно есть
        // кладём новое значение в первую свободную ячейку (по индексу size)
        data[size] = value;

        // увеличиваем счётчик элементов на 1
        size++;
    }
    //Метод remove(int index) — удалить элемент по индексу
    public void remove(int index) {
        // проверяем, что индекс существует
        if (index < 0 || index >= size) {
            throw new IndexOutOfBoundsException("Индекс " + index + " вне границ. Размер: " + size);
        }
        // Сдвигаем элементы влево, начиная с удаляемого
        // i идёт от index до size-2 (предпоследний элемент)
        for (int i = index; i < size - 1; i++) {
            // на место текущего элемента кладём следующий
            data[i] = data[i + 1];
        }
        // последний элемент (который стал дублем) затираем нулём (необязательно, но красиво)
        data[size - 1] = 0;
        // уменьшаем счётчик элементов
        size--;
    }
    // Метод print() — вывести массив в консоль
    public void print() {
        // печатаем открывающую квадратную скобку
        System.out.print("[");

        // проходим только по РЕАЛЬНЫМ элементам (от 0 до size-1)
        for (int i = 0; i < size; i++) {
            // печатаем элемент
            System.out.print(data[i]);

            // если это не последний элемент — печатаем запятую и пробел
            if (i < size - 1) {
                System.out.print(", ");
            }
        }

        // печатаем закрывающую скобку и перевод строки
        System.out.println("]");
    }

    public static void main(String[] args) {
        DynamicArray2 arr = new DynamicArray2();

        arr.add(10);
        arr.add(20);
        arr.add(30);
        arr.print();              // [10, 20, 30]

        System.out.println(arr.size());  // 3
        System.out.println(arr.get(1));  // 20

        arr.remove(1);
        arr.print();              // [10, 30]
    }


}



