// Объявляем пакет (папку, где лежит файл)
package queue;

/**
 * ОЧЕРЕДЬ (Queue) — структура данных FIFO
 * FIFO = First In, First Out (первый пришёл — первый ушёл)
 *
 * Как в очереди в магазине:
 * - новые люди встают в КОНЕЦ
 * - обслуживают с НАЧАЛА
 */
public class MyQueue {

    // ==========================================================
    // ПОЛЯ КЛАССА (что хранит очередь внутри)
    // ==========================================================

    // ВНУТРЕННИЙ МАССИВ для хранения чисел
    // private — никто снаружи не лазит
    // int[] — массив целых чисел
    // data — имя переменной
    // new int[2] — начальный размер 2 ячейки (потом расширим)
    private int[] data = new int[2];

    // СЧЁТЧИК — сколько элементов РЕАЛЬНО лежит в очереди
    // size = 0 — очередь пуста
    private int size = 0;

    // ИНДЕКС НАЧАЛА ОЧЕРЕДИ — откуда будем забирать элементы
    // При удалении мы не сдвигаем весь массив, а просто двигаем этот указатель
    // start = 0 — начинаем с первой ячейки
    private int start = 0;

    // ==========================================================
    // МЕТОД enqueue() — ДОБАВИТЬ ЭЛЕМЕНТ В КОНЕЦ ОЧЕРЕДИ
    // ==========================================================
    public void enqueue(int value) {

        // ШАГ 1: ПРОВЕРЯЕМ, ЕСТЬ ЛИ МЕСТО В МАССИВЕ
        // Если количество элементов (size) равно длине массива — значит, массив заполнен
        if (size == data.length) {

            // СОЗДАЁМ НОВЫЙ МАССИВ БОЛЬШЕГО РАЗМЕРА (в 2 раза)
            int[] newData = new int[data.length * 2];

            // КОПИРУЕМ СТАРЫЕ ЭЛЕМЕНТЫ В НОВЫЙ МАССИВ
            // Но старые элементы могут быть НЕ с начала!
            // Например: было [_, 10, 20, _], start=1, size=2
            // Нужно скопировать: newData[0]=10, newData[1]=20
            for (int i = 0; i < size; i++) {
                // data[(start + i) % data.length] — берём элемент из старого массива
                // start + i — сдвигаем от начала очереди
                // % data.length — зацикливаем, если дошли до конца
                newData[i] = data[(start + i) % data.length];
            }

            // ЗАМЕНЯЕМ СТАРЫЙ МАССИВ НОВЫМ
            data = newData;

            // СБРАСЫВАЕМ УКАЗАТЕЛЬ НАЧАЛА (теперь очередь начинается с индекса 0)
            start = 0;
        }

        // ШАГ 2: ОПРЕДЕЛЯЕМ, КУДА КЛАСТЬ НОВЫЙ ЭЛЕМЕНТ
        // Конец очереди = (начало + размер) по модулю длины массива
        // Например: start=0, size=2, data.length=4 → end = (0+2)%4 = 2 (третья ячейка)
        int end = (start + size) % data.length;

        // ШАГ 3: КЛАДЁМ ЭЛЕМЕНТ В КОНЕЦ
        data[end] = value;

        // ШАГ 4: УВЕЛИЧИВАЕМ СЧЁТЧИК ЭЛЕМЕНТОВ
        size++;
    }

    // ==========================================================
    // МЕТОД dequeue() — ВЗЯТЬ ЭЛЕМЕНТ ИЗ НАЧАЛА ОЧЕРЕДИ И УДАЛИТЬ ЕГО
    // ==========================================================
    public int dequeue() {

        // ШАГ 1: ПРОВЕРЯЕМ, ЕСТЬ ЛИ ЭЛЕМЕНТЫ В ОЧЕРЕДИ
        if (size == 0) {
            System.out.println("Ошибка: очередь пуста");
            return -1;  // заглушка
        }

        // ШАГ 2: ЗАПОМИНАЕМ ЭЛЕМЕНТ, КОТОРЫЙ БЕРЁМ
        // Берём элемент по индексу start (начало очереди)
        int result = data[start];

        // ШАГ 3: СДВИГАЕМ УКАЗАТЕЛЬ НАЧАЛА ВПРАВО
        // Теперь очередь начинается со следующего элемента
        start = (start + 1) % data.length;

        // ШАГ 4: УМЕНЬШАЕМ СЧЁТЧИК ЭЛЕМЕНТОВ
        size--;

        // ШАГ 5: ВОЗВРАЩАЕМ ЗАПОМНЕННЫЙ ЭЛЕМЕНТ
        return result;
    }

    // ==========================================================
    // МЕТОД peek() — ПОСМОТРЕТЬ ПЕРВЫЙ ЭЛЕМЕНТ (НЕ УДАЛЯЯ)
    // ==========================================================
    public int peek() {
        // Проверяем, есть ли элементы
        if (size == 0) {
            System.out.println("Ошибка: очередь пуста");
            return -1;
        }

        // Возвращаем элемент из начала, но НЕ сдвигаем start и НЕ уменьшаем size
        return data[start];
    }

    // ==========================================================
    // МЕТОД isEmpty() — ПРОВЕРЯЕТ, ПУСТА ЛИ ОЧЕРЕДЬ
    // ==========================================================
    public boolean isEmpty() {
        return size == 0;
    }

    // ==========================================================
    // МЕТОД size() — ВОЗВРАЩАЕТ КОЛИЧЕСТВО ЭЛЕМЕНТОВ
    // ==========================================================
    public int size() {
        return size;
    }

    // ==========================================================
    // МЕТОД print() — ПЕЧАТАЕТ ВСЮ ОЧЕРЕДЬ (для отладки)
    // ==========================================================
    public void print() {
        System.out.print("[");
        for (int i = 0; i < size; i++) {
            // Берём элемент по индексу (start + i) с зацикливанием
            int index = (start + i) % data.length;
            System.out.print(data[index]);
            if (i < size - 1) {
                System.out.print(", ");
            }
        }
        System.out.println("]");
    }
}