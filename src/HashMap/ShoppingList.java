package HashMap;

import java.util.HashMap;  // импортируем HashMap

public class ShoppingList {
    public static void main(String[] args) {

        // ========== 1. СОЗДАЁМ СПИСОК ПОКУПОК ==========
        // Ключ: название продукта (String)
        // Значение: количество (Integer)
        HashMap<String, Integer> list = new HashMap<>();

        // ========== 2. ДОБАВЛЯЕМ ПРОДУКТЫ ==========
        // put(ключ, значение) — добавить или заменить
        list.put("яблоки", 3);     // 3 яблока
        list.put("бананы", 2);     // 2 банана
        list.put("молоко", 1);     // 1 пакет молока
        list.put("хлеб", 1);       // 1 батон хлеба

        // ========== 3. СМОТРИМ ВЕСЬ СПИСОК ==========
        System.out.println("=== Весь список покупок ===");
        System.out.println(list);
        // Вывод: {бананы=2, хлеб=1, яблоки=3, молоко=1}

        // ========== 4. УЗНАЁМ, СКОЛЬКО КУПИТЬ ЯБЛОК ==========
        // get(ключ) — получить значение по ключу
        int apples = list.get("яблоки");
        System.out.println("\nЯблок нужно купить: " + apples + " шт.");

        // ========== 5. ПРОВЕРЯЕМ, ЕСТЬ ЛИ ПРОДУКТ В СПИСКЕ ==========
        // containsKey(ключ) — проверяет наличие ключа
        if (list.containsKey("бананы")) {
            System.out.println("Бананы есть в списке");
        }

        // ========== 6. ДОБАВЛЯЕМ ЕЩЁ ЯБЛОК (УВЕЛИЧИВАЕМ КОЛИЧЕСТВО) ==========
        // Для этого: забираем текущее значение, прибавляем, кладём обратно
        int current = list.get("яблоки");
        list.put("яблоки", current + 2);  // было 3, стало 5
        System.out.println("\nДобавили ещё 2 яблока. Теперь: " + list.get("яблоки"));

        // ========== 7. ПОКУПКУ СДЕЛАЛИ — УДАЛЯЕМ ПРОДУКТ ИЗ СПИСКА ==========
        list.remove("молоко");
        System.out.println("\nМолоко купили, удаляем из списка");
        System.out.println("Осталось купить: " + list);

        // ========== 8. ПЕЧАТАЕМ СПИСОК КРАСИВО ==========
        System.out.println("\n=== ОСТАЛОСЬ КУПИТЬ ===");
        for (String product : list.keySet()) {
            System.out.println(product + " — " + list.get(product) + " шт.");
        }

        // ========== 9. СКОЛЬКО ВСЕГО ПОЗИЦИЙ В СПИСКЕ ==========
        System.out.println("\nВсего позиций в списке: " + list.size());
    }
}
